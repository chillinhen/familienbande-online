sub getKinderarzt
{
	my $id=shift;
	my $q_string="id_link='$id'";
	if(length($id)<5)
	{
		&main::logge_int("Ungültige kurze ID: $id (von $ENV{'HTTP_REFERER'})");
		return;
		if(exists($main::cache->{'kinderarzt_linkids'}->{$id}))
		{
			$id=$main::cache->{'kinderarzt_linkids'}->{$id};
			$q_string="id_link='$id'";
		} else
		{
			$q_string="id=$id";
		}
	}
	if(exists($main::cache->{'db'}->{'kinderarzt'}->{$id}) && !-e "$main::master->{'_path_data'}/kinderarzt/tmp/$id")
	{
		return($main::cache->{'db'}->{'kinderarzt'}->{$id});
	}
	&db_connect();
	my $q="select * from baby.kinderarzt where $q_string";
	#&main::logge_int("Q: $q");
	my $erg=$main::DB->query($q);
	my %e=$erg->fetchhash;
	$e{'titel'}=~s/ ?FAK ?//;
	$main::cache->{'db'}->{'kinderarzt'}->{$id}=\%e;
	if(length($id)>4)
	{
		$main::cache->{'kinderarzt_linkids'}->{$e{'id'}}=$id;
		#&main::logge_int("Neue Kinderarzt-Linkid 1: $e{'id'}	=	$e{'id_link'}");
	} else
	{
		$main::cache->{'kinderarzt_linkids'}->{$e{'id'}}=$e{'id_link'};
		#&main::logge_int("Neue Kinderarzt-Linkid 2: $e{'id'}	=	$e{'id_link'}");
	}
	return($main::cache->{'db'}->{'kinderarzt'}->{$id});
}

