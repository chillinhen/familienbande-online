sub getArztName
{
	my $arzt=shift;
	my $titel_nur_dr=shift;
	my $ohne_anrede=shift;
	my $ohne_vorname=shift;
	my $kein_titel=shift;
	$arzt->{'titel'}=~s/ ?FAK ?//;
	my $name="";
	if($ohne_anrede<1)
	{
		if($arzt->{'geschlecht'} eq "m")
		{
			$name="Herr ";
		} elsif($arzt->{'geschlecht'} eq "w")
		{
			$name="Frau ";
		}
	}
	if(!$kein_titel && length($arzt->{'titel'})>0)
	{
		if($titel_nur_dr>0)
		{ 
			 $arzt->{'titel'}=~s/Dr\. med\./Dr./i;
			
		}
		$name.="$arzt->{'titel'} ";
	}
	if(length($arzt->{'vorname'})>0 and !$ohne_vorname)
	{
		$name.="$arzt->{'vorname'} ";
	}
	if(length($arzt->{'nachname'})>0)
	{
		$name.="$arzt->{'nachname'} ";
	}
	$name=~s/\s+$//;
	return($name);
} 
