sub id2code
{
	my $id=shift;
	my $neu=int(rand(8))+1;
	$neu.=length($id);
	$neu.=int(rand(8))+1;
	$neu.=$id;
	$neu.=int(rand(8))+1;
	return($neu);
}

sub code2id
{
	my $code=shift;
	my $l=substr($code,1,1);
	return(substr($code,3,$l));
}

sub getRubrikenSprueche
{
	my @r;
	my @keys=&mischeListe(keys %$R);
	foreach my $i (@keys)
	{
		my @temp=&main::mischeListe(@{$R->{$i}->{'titelworte'}});
		#&main::logge_int("E: $i / $temp[0]");
		push(@r,{
			titel	=> shift(@temp),
			link		=> $R->{$i}->{'link'},
			rubrik	=> $i
		});
	}
	return(@r);
}

sub getRubrik
{
	my ($kategorie,$subkategorie,$zufall)=@_;
	$kategorie="geburt_gedichte" if $kategorie eq "geb_gedichte";
	$kategorie="geburt_sprueche" if $kategorie eq "geb_sprueche";
	$kategorie="sprueche_silvester" if $kategorie eq "silvester";
	$kategorie="geburt_glueckwuensche" if $kategorie eq "geb_glueckwuensche";
	$kategorie=~s/^(familie|geburtstag|hochzeit|aphorismen|muttertag|zungenbrecher|geburstag|bauernregeln|weihnacht|winter|liebessprueche|liebe|vatertag)/sprueche_$1/;
	$kategorie=~s/^(gebete)$/gebete_kinder/;

	if(!exists($R->{$kategorie}))
	{
		&main::logge_int("Fehler: Rubrik $kategorie existiert nicht! / $ENV{'HTTP_REFERER'}");
		return({});
	}
	return($R->{$kategorie});

}
sub getRubrikname
{
	my ($kategorie,$subkategorie,$zufall)=@_;
	my @temp;

	&main::logge_int("Suche $kategorie");
	if(!$subkategorie)
	{
		if(exists($R->{$kategorie}->{'titelworte'}))
		{
			@temp=@{$R->{$kategorie}->{'titelworte'}};
		}
	}
	if($#temp<0)
	{
		return($kategorie);
	} elsif($zufall)
	{
		my @t=&main::mischeListe(@temp);
		return(shift(@t));
	}
	return(shift(@temp));
}


