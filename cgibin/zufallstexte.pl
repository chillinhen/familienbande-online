sub getZufallstext
{
	my ($text,$gross,$ersatz)=@_;
	if($text eq "_alles")
	{
		return(\%worte);
	}
	
	my @temp;
	my $antwort;
	$worte{$text}=~s/\n//g;
	if(length($worte{$text})>0)
	{
		@temp=split(/,/,$worte{$text});
		my $anz=($#temp+1);
		my $id=int(rand($anz))-1;
		$antwort=$temp[$id];
		$antwort="\u$antwort" if $gross>0;
		$antwort="\l$antwort" if $gross>1;
		$antwort=~s/##ersatz##/$ersatz/g;
	} else
	{
		warn("Unbekannter Zufallstext: $text");
	}
	return($antwort);
}

